//-----------------------------------------------------------------------------
// Initial
//-----------------------------------------------------------------------------
const initialState = {
  id: null,
  value: null
}

//-----------------------------------------------------------------------------
// Reducers
//-----------------------------------------------------------------------------
const viewReducers = (state = initialState, action) => {
  switch(action.type) {
      
    case 'SET_NOTE': {
      const {
        note
      } = action
      return note
    }

    default:
      return state
  }
}

export default viewReducers